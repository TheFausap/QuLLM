"""Dataset-size sweep for phase/unitary relational compatibility.

The experiment uses a synthetic teacher whose labels are generated by Born-rule
overlap after a relation-specific diagonal unitary. Student models then try to
learn the relation from different training-set sizes.

This is intentionally controlled: if a phase-native inductive bias cannot win
here, it is unlikely to help on messier language data.
"""

from __future__ import annotations

import argparse
import csv
import json
import math
from pathlib import Path
import random
import time
from typing import Any

try:
    import torch
    from torch import nn
    from torch.nn import functional as F
except ModuleNotFoundError as exc:  # pragma: no cover - exercised by users without torch
    raise SystemExit(
        "This experiment requires PyTorch. Install it with:\n"
        "  python3 -m pip install -r requirements.txt\n"
        "On DGX/CUDA, install the PyTorch build matching that system."
    ) from exc


ROOT = Path(__file__).resolve().parents[1]


def choose_device(requested: str) -> torch.device:
    if requested != "auto":
        return torch.device(requested)
    if torch.cuda.is_available():
        return torch.device("cuda")
    if torch.backends.mps.is_available():
        return torch.device("mps")
    return torch.device("cpu")


def load_config(path: str | None) -> dict[str, Any]:
    if not path:
        return {}
    with open(path, "r", encoding="utf-8") as handle:
        return json.load(handle)


def set_seed(seed: int) -> None:
    random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)


def complex_normalize(real: torch.Tensor, imag: torch.Tensor, eps: float = 1e-8) -> tuple[torch.Tensor, torch.Tensor]:
    scale = torch.sqrt((real * real + imag * imag).sum(dim=-1, keepdim=True).clamp_min(eps))
    return real / scale, imag / scale


def born_overlap(
    left_real: torch.Tensor,
    left_imag: torch.Tensor,
    right_real: torch.Tensor,
    right_imag: torch.Tensor,
) -> torch.Tensor:
    # <left|right> = sum(conj(left) * right)
    real = (left_real * right_real + left_imag * right_imag).sum(dim=-1)
    imag = (left_real * right_imag - left_imag * right_real).sum(dim=-1)
    return real * real + imag * imag


class PhaseTeacher:
    def __init__(self, vocab_size: int, dim: int, relations: int, device: torch.device, seed: int) -> None:
        generator = torch.Generator(device="cpu").manual_seed(seed)
        amp = torch.rand(vocab_size, dim, generator=generator).sqrt()
        phase = torch.rand(vocab_size, dim, generator=generator) * math.tau

        real = amp * torch.cos(phase)
        imag = amp * torch.sin(phase)
        real, imag = complex_normalize(real, imag)

        rel_phase = torch.rand(relations, dim, generator=generator) * math.tau

        self.token_real = real.to(device)
        self.token_imag = imag.to(device)
        self.rel_phase = rel_phase.to(device)
        self.threshold = self._estimate_balanced_threshold(vocab_size, relations, device)

    @torch.no_grad()
    def _estimate_balanced_threshold(self, vocab_size: int, relations: int, device: torch.device) -> float:
        left, rel, right = sample_triples(65536, vocab_size, relations, device)
        scores = self.score(left, rel, right)
        return float(scores.median().item())

    @torch.no_grad()
    def score(self, left: torch.Tensor, rel: torch.Tensor, right: torch.Tensor) -> torch.Tensor:
        left_real = self.token_real[left]
        left_imag = self.token_imag[left]
        right_real = self.token_real[right]
        right_imag = self.token_imag[right]

        phase = self.rel_phase[rel]
        c = torch.cos(phase)
        s = torch.sin(phase)
        rotated_real = right_real * c - right_imag * s
        rotated_imag = right_real * s + right_imag * c
        return born_overlap(left_real, left_imag, rotated_real, rotated_imag)

    @torch.no_grad()
    def label(self, left: torch.Tensor, rel: torch.Tensor, right: torch.Tensor) -> torch.Tensor:
        return (self.score(left, rel, right) > self.threshold).float()


def sample_triples(
    count: int,
    vocab_size: int,
    relations: int,
    device: torch.device,
) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
    left = torch.randint(0, vocab_size, (count,), device=device)
    rel = torch.randint(0, relations, (count,), device=device)
    right = torch.randint(0, vocab_size, (count,), device=device)
    return left, rel, right


def make_dataset(
    teacher: PhaseTeacher,
    count: int,
    vocab_size: int,
    relations: int,
    device: torch.device,
) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
    left, rel, right = sample_triples(count, vocab_size, relations, device)
    labels = teacher.label(left, rel, right)
    return left, rel, right, labels


class PhaseUnitaryModel(nn.Module):
    def __init__(self, vocab_size: int, dim: int, relations: int) -> None:
        super().__init__()
        self.amp_logits = nn.Embedding(vocab_size, dim)
        self.token_phase = nn.Embedding(vocab_size, dim)
        self.rel_phase = nn.Embedding(relations, dim)
        self.logit_scale = nn.Parameter(torch.tensor(8.0))
        self.logit_bias = nn.Parameter(torch.tensor(0.0))
        nn.init.normal_(self.amp_logits.weight, std=0.2)
        nn.init.uniform_(self.token_phase.weight, -0.1, 0.1)
        nn.init.uniform_(self.rel_phase.weight, -0.1, 0.1)

    def token_state(self, token_ids: torch.Tensor) -> tuple[torch.Tensor, torch.Tensor]:
        amp = F.softplus(self.amp_logits(token_ids)) + 1e-5
        phase = self.token_phase(token_ids)
        real = amp * torch.cos(phase)
        imag = amp * torch.sin(phase)
        return complex_normalize(real, imag)

    def forward(self, left: torch.Tensor, rel: torch.Tensor, right: torch.Tensor) -> torch.Tensor:
        left_real, left_imag = self.token_state(left)
        right_real, right_imag = self.token_state(right)

        phase = self.rel_phase(rel)
        c = torch.cos(phase)
        s = torch.sin(phase)
        rotated_real = right_real * c - right_imag * s
        rotated_imag = right_real * s + right_imag * c
        score = born_overlap(left_real, left_imag, rotated_real, rotated_imag)
        return self.logit_scale * score + self.logit_bias


class PhaseScrambledModel(PhaseUnitaryModel):
    """Ablation: token phases exist, but relation phases are not learnable."""

    def __init__(self, vocab_size: int, dim: int, relations: int) -> None:
        super().__init__(vocab_size, dim, relations)
        self.rel_phase.weight.requires_grad_(False)
        with torch.no_grad():
            self.rel_phase.weight.zero_()


class AmplitudeOnlyModel(nn.Module):
    def __init__(self, vocab_size: int, dim: int, relations: int) -> None:
        super().__init__()
        self.amp_logits = nn.Embedding(vocab_size, dim)
        self.rel_gate = nn.Embedding(relations, dim)
        self.logit_scale = nn.Parameter(torch.tensor(8.0))
        self.logit_bias = nn.Parameter(torch.tensor(0.0))
        nn.init.normal_(self.amp_logits.weight, std=0.2)
        nn.init.normal_(self.rel_gate.weight, std=0.02)

    def token_state(self, token_ids: torch.Tensor) -> torch.Tensor:
        amp = F.softplus(self.amp_logits(token_ids)) + 1e-5
        return F.normalize(amp, dim=-1)

    def forward(self, left: torch.Tensor, rel: torch.Tensor, right: torch.Tensor) -> torch.Tensor:
        left_state = self.token_state(left)
        right_state = self.token_state(right)
        gate = torch.sigmoid(self.rel_gate(rel))
        score = (left_state * gate * right_state).sum(dim=-1).square()
        return self.logit_scale * score + self.logit_bias


class RealDiagModel(nn.Module):
    def __init__(self, vocab_size: int, dim: int, relations: int) -> None:
        super().__init__()
        self.left = nn.Embedding(vocab_size, dim)
        self.right = nn.Embedding(vocab_size, dim)
        self.rel = nn.Embedding(relations, dim)
        self.logit_scale = nn.Parameter(torch.tensor(1.0))
        self.logit_bias = nn.Parameter(torch.tensor(0.0))
        nn.init.normal_(self.left.weight, std=dim**-0.5)
        nn.init.normal_(self.right.weight, std=dim**-0.5)
        nn.init.normal_(self.rel.weight, std=dim**-0.5)

    def forward(self, left: torch.Tensor, rel: torch.Tensor, right: torch.Tensor) -> torch.Tensor:
        score = (self.left(left) * self.rel(rel) * self.right(right)).sum(dim=-1)
        return self.logit_scale * score + self.logit_bias


MODEL_TYPES = {
    "phase_unitary": PhaseUnitaryModel,
    "real_diag": RealDiagModel,
    "amplitude_only": AmplitudeOnlyModel,
    "phase_scrambled": PhaseScrambledModel,
}


@torch.no_grad()
def evaluate(
    model: nn.Module,
    dataset: tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor],
    batch_size: int,
) -> dict[str, float]:
    model.eval()
    left, rel, right, labels = dataset
    losses: list[torch.Tensor] = []
    correct = 0
    total = labels.numel()

    for start in range(0, total, batch_size):
        batch = slice(start, start + batch_size)
        logits = model(left[batch], rel[batch], right[batch])
        loss = F.binary_cross_entropy_with_logits(logits, labels[batch], reduction="sum")
        losses.append(loss)
        correct += ((torch.sigmoid(logits) >= 0.5) == labels[batch].bool()).sum().item()

    return {
        "loss": float(torch.stack(losses).sum().item() / total),
        "accuracy": correct / total,
    }


def train_one(
    model_name: str,
    train_data: tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor],
    test_data: tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor],
    vocab_size: int,
    dim: int,
    relations: int,
    epochs: int,
    batch_size: int,
    learning_rate: float,
    device: torch.device,
) -> dict[str, float | str | int]:
    model = MODEL_TYPES[model_name](vocab_size, dim, relations).to(device)
    optimizer = torch.optim.AdamW(model.parameters(), lr=learning_rate, weight_decay=1e-4)
    left, rel, right, labels = train_data
    count = labels.numel()
    started = time.time()

    for _epoch in range(epochs):
        model.train()
        order = torch.randperm(count, device=device)
        for start in range(0, count, batch_size):
            idx = order[start : start + batch_size]
            logits = model(left[idx], rel[idx], right[idx])
            loss = F.binary_cross_entropy_with_logits(logits, labels[idx])
            optimizer.zero_grad(set_to_none=True)
            loss.backward()
            optimizer.step()

    metrics = evaluate(model, test_data, batch_size)
    metrics.update(
        {
            "model": model_name,
            "train_size": count,
            "seconds": round(time.time() - started, 3),
        }
    )
    return metrics


def parse_train_sizes(value: str | list[int]) -> list[int]:
    if isinstance(value, list):
        return [int(item) for item in value]
    return [int(item.strip()) for item in value.split(",") if item.strip()]


def write_results(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fields = ["train_size", "model", "accuracy", "loss", "seconds"]
    with open(path, "w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row[field] for field in fields})


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--config", type=str, default=None)
    parser.add_argument("--device", type=str, default="auto")
    parser.add_argument("--vocab-size", type=int, default=2048)
    parser.add_argument("--dim", type=int, default=32)
    parser.add_argument("--relations", type=int, default=8)
    parser.add_argument("--train-sizes", type=str, default="512,2048,8192,32768")
    parser.add_argument("--test-size", type=int, default=8192)
    parser.add_argument("--epochs", type=int, default=12)
    parser.add_argument("--batch-size", type=int, default=512)
    parser.add_argument("--learning-rate", type=float, default=3e-3)
    parser.add_argument("--seed", type=int, default=7)
    parser.add_argument("--models", type=str, default="phase_unitary,real_diag,amplitude_only,phase_scrambled")
    parser.add_argument("--out", type=str, default="runs/phase_relation_scaling.csv")
    args = parser.parse_args()

    config = load_config(args.config)
    vocab_size = int(config.get("vocab_size", args.vocab_size))
    dim = int(config.get("dim", args.dim))
    relations = int(config.get("relations", args.relations))
    train_sizes = parse_train_sizes(config.get("train_sizes", args.train_sizes))
    test_size = int(config.get("test_size", args.test_size))
    epochs = int(config.get("epochs", args.epochs))
    batch_size = int(config.get("batch_size", args.batch_size))
    learning_rate = float(config.get("learning_rate", args.learning_rate))
    seed = int(config.get("seed", args.seed))
    models_raw = config.get("models", args.models)
    models = models_raw if isinstance(models_raw, list) else [item.strip() for item in models_raw.split(",")]

    unknown = sorted(set(models) - set(MODEL_TYPES))
    if unknown:
        raise SystemExit(f"unknown model(s): {', '.join(unknown)}")

    device = choose_device(args.device)
    set_seed(seed)

    print(f"device={device} vocab={vocab_size} dim={dim} relations={relations}")
    teacher = PhaseTeacher(vocab_size, dim, relations, device, seed=seed + 10_000)
    test_data = make_dataset(teacher, test_size, vocab_size, relations, device)

    rows: list[dict[str, Any]] = []
    for train_size in train_sizes:
        train_data = make_dataset(teacher, train_size, vocab_size, relations, device)
        print(f"\ntrain_size={train_size}")
        for model_name in models:
            set_seed(seed)
            row = train_one(
                model_name=model_name,
                train_data=train_data,
                test_data=test_data,
                vocab_size=vocab_size,
                dim=dim,
                relations=relations,
                epochs=epochs,
                batch_size=batch_size,
                learning_rate=learning_rate,
                device=device,
            )
            rows.append(row)
            print(
                f"  {model_name:16s} "
                f"acc={row['accuracy']:.4f} loss={row['loss']:.4f} seconds={row['seconds']}"
            )

    out = ROOT / args.out
    write_results(out, rows)
    print(f"\nwrote {out}")


if __name__ == "__main__":
    main()
